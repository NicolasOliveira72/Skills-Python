x = int(input("Digite o primeiro valor:\n "))
y = int(input("Digite o segundo valor:\n "))

# Aritméticos:

soma = x + y
sub = x - y
mult = x * y
div = x / y
res = x % y
exp = x ** y

print(f"Soma: {soma}")
print(f"Subtração: {sub}")
print(f"Multiplicação: {mult}")
print(f"Divisão: {div}")
print(f"Resto da divisão: {res}")
print(f"Exponenciação: {exp}")

# Comparação/Relacinoais:

maior = x > y
